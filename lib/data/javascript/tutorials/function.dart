# Functions

### A function can be defined by providing the name of the function with the appropriate parameters and return type.

The syntax is given below.

**Syntax:**

```javascript
function functionName(parameter1, parameter2) {
    // statement(s)
    return value;
}
  
```
* functionName - The name of the function should be appropriate and follow JavaScript naming conventions.
* parameter1, parameter2 - The list of parameters the function will accept.

* return value - A function can return a value once it completes execution. The return type is implicitly inferred based on the returned value.

## Calling a Function
### After creating a function, we can call or invoke the defined function elsewhere in the code. The syntax is given below.


**Syntax:**

```dart
functionName(argument1, argument2);  
or  
let result = functionName(argument1, argument2); 
