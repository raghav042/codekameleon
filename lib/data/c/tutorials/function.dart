# Functions in C

### A function in C is defined by specifying the return type, function name, and parameters. It allows code to be reused and structured clearly.

The syntax is given below.

**Syntax:**

```c
return_type function_name(parameter_list) {
    // statements
    return value;
}
```
* return_type – Can be any valid C data type like int, void, float, etc.
* function_name – Must be a valid identifier.
* parameter_list – List of input parameters with types.
* return value – If the return type is not void, the function must return a value.

### Calling a Function

After defining a function, it can be called from main() or another function. The syntax is given below.

```C
function_name(argument_list);
// or, to store return value:
variable = function_name(arguments);
```
