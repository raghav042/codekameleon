# Introduction to C: A Powerful Language for System Programming  

### C is a procedural programming language developed by Dennis Ritchie in the 1970s. It is widely used for system programming, embedded systems, and operating systems. C provides low-level memory access and is considered the foundation for many modern programming languages.

---

## **Advantages of C**  
- **Efficient Execution**: Provides fine control over system resources, making it ideal for system-level programming.  
- **Procedural**: Follows a structured approach to problem-solving with functions and procedures.  
- **Memory Management**: Allows manual memory allocation and deallocation using `malloc()` and `free()`.  
- **Portability**: Code written in C can be easily ported to different platforms, making it highly versatile.  

---

### **Example: Running a simple C program**

```c
#include <stdio.h>

int main() {
  printf("Welcome to C!\n");
  return 0;
}
```
This program uses the main() function, which is the entry point of any C application. The printf() function outputs text to the console.

